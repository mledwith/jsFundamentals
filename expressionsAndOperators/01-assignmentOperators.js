/*
ASSIGNMENT OPERATORS
*/

let r = 5
console.log(r);
//our equals sign is the assignment

//ADDITION ASSIGNMENT
r += 1; // r = r + 1 or, 5 + 1 in this case
console.log(r);

//SUBTRACTION ASSIGNMENT
r -= 2; // r = r - 2
console.log(r);

//MULTIPLICATION ASSIGNMENT
r *= 2; // r = r * 2
console.log(r);

//DIVISION ASSIGNMENT
r /= 1; // r = r / 1
console.log(r);

//EXPONENTIAL ASSIGNMENT
r **= 3; // r = r * r * r
console.log(r);

//REMAINDER (modulus) ASSIGNMENT
r %= 1; // r = r % 1    
console.log(r);

let y = 5;
console.log(y);
y %= 2;
console.log(y);
